package day04_concatenation;

public class PersonalInfo {
    public static void main(String[] args) {
        String firstName = "Tatiana";
        String lastName = "Kuvshynova";
        char gender = 'F';
        int age = 27;
        String from = "Ukraine",
                favBook = "\"Gone with the Wind\"",
                favShow = "\"Breaking Bad\"",
                favMusic = "rock",
                favColor = "blue",
                marriedStatus = "single";
        boolean isEmployed = true;


        System.out.println("firstName = " + firstName);
        System.out.println("lastName = " + lastName);
        System.out.println("gender = " + gender);
        System.out.println("age = " + age);
        System.out.println("from = " + from);
        System.out.println("favMusic = " + favMusic);
        System.out.println("favShow = " + favShow);
        System.out.println("favShow = " + favShow);
        System.out.println("favColor = " + favColor);
        System.out.println("marriedStatus = " + marriedStatus);
        System.out.println("isEmployed = " + isEmployed);
    }
}
