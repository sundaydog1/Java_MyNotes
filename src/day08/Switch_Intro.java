package day08;

public class Switch_Intro {
    public static void main(String[] args) {
        char grade = 'A';

        switch (grade) {
            case 'A':
                System.out.println("Excellent");
                case 'B':
                    System.out.println("Great job");
            default:
                System.out.println( "Whatever");


        }



        }


    }


